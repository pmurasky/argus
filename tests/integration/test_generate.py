def test_claude_md_points_to_rules_directory(tmp_path):
    (tmp_path / ".argus.yml").write_text(FULL_CONFIG)
    runner = CliRunner()
    runner.invoke(main, ["generate", "--project-root", str(tmp_path)])
    content = (tmp_path / "CLAUDE.md").read_text()
    assert ".claude/rules/" in content
    assert "Never write or modify production code" not in content


def test_check_mode_exits_nonzero_on_stale_files(tmp_path):
    (tmp_path / ".argus.yml").write_text(FULL_CONFIG)
    runner = CliRunner()
    runner.invoke(main, ["generate", "--project-root", str(tmp_path)])
    (tmp_path / "CLAUDE.md").write_text("# manually edited")
    result = runner.invoke(main, ["generate", "--check", "--project-root", str(tmp_path)])
    assert result.exit_code == 1


def test_check_mode_exits_zero_when_files_current(tmp_path):
    (tmp_path / ".argus.yml").write_text(FULL_CONFIG)
    runner = CliRunner()
    runner.invoke(main, ["generate", "--project-root", str(tmp_path)])
    result = runner.invoke(main, ["generate", "--check", "--project-root", str(tmp_path)])
    assert result.exit_code == 0


