from click.testing import CliRunner

from argus.cli import main

FULL_CONFIG = """\
packs:
  - tdd
  - atomic-commit
platforms:
  - claude
  - opencode
  - copilot
  - cursor
"""

GEMINI_CONFIG = """\
packs:
  - tdd
platforms:
  - gemini
"""


def test_generate_produces_claude_files(tmp_path):
    (tmp_path / ".argus.yml").write_text(FULL_CONFIG)
    runner = CliRunner()
    result = runner.invoke(main, ["generate", "--project-root", str(tmp_path)])
    assert result.exit_code == 0, result.output
    assert (tmp_path / "CLAUDE.md").exists()
    assert (tmp_path / ".claude/rules/tdd.md").exists()
    assert (tmp_path / ".claude/rules/atomic-commit.md").exists()
    assert (tmp_path / ".claude/skills/tdd/SKILL.md").exists()


